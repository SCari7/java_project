package classwork_07;

public class Revers {
    public static void main(String[] args) {
        int countSec = 10;

        System.out.println("We have " + countSec + " sek before start!");

        while (countSec > 0){
            countSec --;
            System.out.println("We have " + countSec + " till start");
        }

        System.out.println("Here we go!");
    }

}
