package classwork_03;

public class mathOperations {
    public static void main(String[] args) {
        int var1 = 20;
        int var2 = 7;

        int result;

        result = var1 + var2;
        System.out.println("result: + " + result);

        result = 15 + 6;

        System.out.println("result: + " + result); // ctrl + d copi
        // shift +

        result = var1 + 5;
        System.out.println("result: + " + result); // ctrl + d copi

        result = result + 5;
        System.out.println("result: + " + result);

        result = var1 - var2;

        System.out.println("result: - " + result);

        result = var1 * var2;
        System.out.println("result: * " + result);
        result = (1 + 2) * 2;

        System.out.println("result 1 + 2 * 2 : " + result);

    }


}
